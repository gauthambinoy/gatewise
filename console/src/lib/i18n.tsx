// Exported so the test suite can assert every locale has exactly the same key set as English
// (the source of truth) — catching any missing or stray translation at build time.
export const DICTS: Record<Locale, Record<Key, string>> = { en: EN, de: DE, fr: FR, es: ES, it: IT }

// Module-level active Intl locale, so plain formatters (money/dt) read the current language.
let activeIntl = 'en-GB'
export function intlLocale(): string {
  return activeIntl
}

export type TFunc = (key: Key, vars?: Record<string, string | number>) => string

interface I18n {
  locale: Locale
  setLocale: (l: Locale) => void
  t: TFunc
}

const Ctx = createContext<I18n | null>(null)

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(
    () => (localStorage.getItem('auvex.locale') as Locale) || 'en',
  )
  activeIntl = LOCALES.find((l) => l.code === locale)?.intl ?? 'en-GB'

  useEffect(() => {
    document.documentElement.lang = locale
  }, [locale])

  const value = useMemo<I18n>(() => {
    const dict = DICTS[locale]
    return {
      locale,
      setLocale: (l) => {
        localStorage.setItem('auvex.locale', l)
        activeIntl = LOCALES.find((x) => x.code === l)?.intl ?? 'en-GB'
        setLocaleState(l)
      },
      t: (key, vars) => {
        let s = dict[key] ?? EN[key] ?? String(key)
        if (vars) for (const k of Object.keys(vars)) s = s.replace(`{${k}}`, String(vars[k]))
        return s
      },
    }
  }, [locale])

  return <Ctx.Provider value={value}>{children}</Ctx.Provider>
}

export function useT() {
  const ctx = useContext(Ctx)
  if (!ctx) throw new Error('useT must be used within I18nProvider')
  return ctx
}
